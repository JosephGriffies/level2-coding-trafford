#Arithmetic Operators
i=10
i += 2 #adds 2 to the value of i (12)
i -= 4 #subtracts 4 from the value of i (8)
i *= 30 #Multiplies i by 30 (240)
i /= 6 #divides i by 6 and converts i in to a float (40)
i %= 7 #sets i to the remainder left over by i divided by 3 (5)
i **= 2 #sets i to the value of i squared (25)
i //= 3 #divides i by 3 (25/3) and rounds the number to the nearest integer (8)


print (i)